
public class Operacoes {
	
	public void soma ( int x1 , int x2 , int x3 ) {
		
		int resultado = x1 + x2 + x3;
		System.out.println(" o resultado da soma e " + resultado);
		
	}
    public void subtracao (int x1, int x2) {
    	int resultado = x1 - x2;
    	System.out.print(" o resultado da subtracao e " + resultado);
    	
    }
    public void divisao (int x1, int x2) {    	
    	int resuitado = x1 / x2;
    	System.out.print(" o resultado da divisao e " + resuitado);
    	
    }
    public void multiplicacao (int x1, int x2) {    	
    	int resultado = x1 * x2;
    	System.out.print(" o resultado da multiplicacao e " + resultado);
    	
    }
}
